// Simple if
let age = 20;

if (age >= 18) {
  console.log("You are an adult");
}


// if...else
let number = 5;

if (number % 2 === 0) {
  console.log("Even number");
} else {
  console.log("Odd number");
}


// if...else if...else
let marks = 72;

if (marks >= 90) {
  console.log("Grade A");
} else if (marks >= 70) {
  console.log("Grade B");
} else if (marks >= 50) {
  console.log("Grade C");
} else {
  console.log("Fail");
}
