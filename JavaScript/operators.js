// Arithmetic 
let a = 10;
let b = 3;

console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.333
console.log(a % b); // 1


// Assignment 
let x = 5;
x += 2; // x = x + 2
console.log(x); // 7

x *= 3; // x = x * 3
console.log(x); // 21


// Comparison 
let p = 10;
let q = "10";

console.log(p == q);   // true (only value)
console.log(p === q);  // false (value + type)
console.log(p > 5);    // true
console.log(p <= 9);   // false


// Logical 
let age = 18;
let hasID = true;

console.log(age >= 18 && hasID); // true
console.log(age < 18 || hasID);  // true
console.log(!hasID);             // false
